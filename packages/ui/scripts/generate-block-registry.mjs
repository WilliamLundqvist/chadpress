import fs from "node:fs"
import path from "node:path"
import { fileURLToPath } from "node:url"

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..")
const blocksDir = path.join(root, "blocks")
const registryPath = path.join(blocksDir, "generated-registry.ts")
const indexPath = path.join(blocksDir, "index.ts")

function toIdentifier(folderName) {
  return `block_${folderName.replace(/[^a-zA-Z0-9_$]/g, "_")}Definition`
}

function toNamespaceIdentifier(folderName) {
  const identifier = folderName
    .replace(/-([a-zA-Z0-9])/g, (_, character) => character.toUpperCase())
    .replace(/[^a-zA-Z0-9_$]/g, "_")
  return /^[a-zA-Z_$]/.test(identifier) ? identifier : `_${identifier}`
}

function readBlockMeta(folderName) {
  const blockPath = path.join(blocksDir, folderName, "block.json")
  return JSON.parse(fs.readFileSync(blockPath, "utf8"))
}

const folders = fs
  .readdirSync(blocksDir, { withFileTypes: true })
  .filter((entry) => entry.isDirectory())
  .map((entry) => entry.name)
  .filter((folderName) => fs.existsSync(path.join(blocksDir, folderName, "block.json")))
  .sort()

const blocks = folders.map((folderName) => ({
  folderName,
  importName: toIdentifier(folderName),
  meta: readBlockMeta(folderName),
}))

const blockNames = new Set()
for (const { folderName, meta } of blocks) {
  if (typeof meta.name !== "string" || meta.name.length === 0) {
    throw new Error(`${folderName}/block.json must define a non-empty name`)
  }
  if (blockNames.has(meta.name)) {
    throw new Error(`Duplicate block name "${meta.name}"`)
  }
  if (!fs.existsSync(path.join(blocksDir, folderName, "index.ts"))) {
    throw new Error(`${folderName} has block.json but no index.ts`)
  }
  blockNames.add(meta.name)
}

const imports = blocks
  .map(
    ({ folderName, importName }) =>
      `import { blockDefinition as ${importName} } from "./${folderName}"`,
  )
  .join("\n")

const entries = blocks
  .map(({ importName, meta }) => `  ${JSON.stringify(meta.name)}: ${importName},`)
  .join("\n")

const registrySource = `${imports}

export const blockRegistry = {
${entries}
} as const

export type BlockName = keyof typeof blockRegistry
`

const namespaceExports = blocks
  .map(
    ({ folderName }) =>
      `export * as ${toNamespaceIdentifier(folderName)} from "./${folderName}"`,
  )
  .join("\n")

const indexSource = `// Generated by scripts/generate-block-registry.mjs. Do not edit by hand.
export * from "./capabilities"
export * from "./layout-ancestors"
export * from "./registry"
export * from "./runtime"
export * from "./types"

${namespaceExports}
`

function writeIfChanged(outputPath, source) {
  if (fs.existsSync(outputPath) && fs.readFileSync(outputPath, "utf8") === source) {
    return false
  }
  fs.writeFileSync(outputPath, source)
  return true
}

const changed = [
  writeIfChanged(registryPath, registrySource) && path.relative(root, registryPath),
  writeIfChanged(indexPath, indexSource) && path.relative(root, indexPath),
].filter(Boolean)

const status = changed.length === 0 ? "Already current" : `Generated ${changed.join(" and ")}`
console.log(`${status} with ${blocks.length} blocks`)
